# Rules of Variable


# Variable ka naam alphabet ya underscore (_) se start hona chahiye
name = "Alishba"
_score = 90

# Variable ka naam number se start nahi ho sakta
# 1name = "Alishba"  # Ye error dega

# Sirf letters (a-z, A-Z), numbers (0-9), aur underscore (_) allow hain
student1 = "Ali"
total_marks = 95

# Variables case-sensitive hote hain (chhoti aur badi letters alag hain)
Name = "Aisha"
name = "Ali"

# Python mein space allow nahi hoti variable kay naam mein
# first name = "Zain"  # Ye error dega
first_name = "Zain"  # Sahi tareeqa


# Variable mein koi bhi special character (!, @, #, $, %, ^, &, *) allow nahi hain
# Keywords (jo Python already use karta hai) ko variable bana kar use nahi kar sakte
# class = "Python"  # Ye error dega
class_name = "Python"

# Variables ko kisi bhi value assign kar sakte hain bina type define kiye
age = 20
pi = 3.14
is_student = True  # Yeh boolean value hai

