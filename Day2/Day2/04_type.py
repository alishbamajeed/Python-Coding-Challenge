# TypeCasting in Python

# Typecasting is the process of converting one data type to another. In Python, we can convert the data type of an object using built-in functions like int(), float(), str(), etc.

# Example: Typecasting in Python

# Convert int to float

num1 = 10
num2 = float(num1)
print(num2)  # Output: 10.0

# Convert float to int

num1 = 10.5
num2 = int(num1)
print(num2)  # Output: 10

# Convert int to string

num1 = 10.3
num2 = str(num1)
print(num2)  # Output: '10'

